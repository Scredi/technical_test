initDb()
const database = getDb()

const insertUser = database.prepare(`
  INSERT OR REPLACE INTO user (uuid, email, first_name, last_name, role, phone, created_at, changed_at, enabled, status, accepted_terms_on, last_login_on)
  VALUES (@uuid, @email, @first_name, @last_name, @role, @phone, @created_at, @changed_at, @enabled, @status, @accepted_terms_on, @last_login_on)
`)

const insertFormality = database.prepare(`
  INSERT OR REPLACE INTO formality (uuid, company, type, owner_uuid, status, creation_date, modification_date)
  VALUES (@uuid, @company, @type, @owner_uuid, @status, @creation_date, @modification_date)
`)

const insertManyUsers = database.transaction(() => {
  for (const u of users) {
    insertUser.run({
      uuid: u.uuid,
      email: u.email,
      first_name: u.first_name,
      last_name: u.last_name,
      role: u.role,
      phone: u.phone,
      created_at: u.created_at,
      changed_at: u.changed_at,
      enabled: u.enabled ? 1 : 0,
      status: u.status,
      accepted_terms_on: u.accepted_terms_on,
      last_login_on: u.last_login_on,
    })
  }
})

const insertManyFormalities = database.transaction(() => {
  for (const f of formalities) {
    insertFormality.run({
      uuid: f.uuid,
      company: f.company,
      type: f.type,
      owner_uuid: f.owner_uuid,
      status: f.status,
      creation_date: f.creation_date,
      modification_date: f.modification_date,
    })
  }
})

insertManyUsers()
insertManyFormalities()
